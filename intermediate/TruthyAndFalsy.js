
/* A falsy value is a value that evaluates to false when converted to a Boolean.

Here are all the falsy values in JavaScript:

1-> false - The Boolean false.

2-> 0 - The number zero.

3-> -0 - The negative zero.

4-> 0n - The BigInt representation of zero.

5-> "" - An empty string (both single and double quotes).

6-> null - The absence of a value.

7-  undefined - A variable that has been declared but not assigned a value.

8->  NaN - Not a Number.
 */
