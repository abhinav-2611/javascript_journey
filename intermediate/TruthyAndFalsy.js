/*A truthy value is any value that is not falsy. Here are examples of truthy values:

1->true - The Boolean true.

2->Any non-zero number (e.g., 1, -1, 3.14).

3->Any non-empty string (e.g., "hello", 'false', "0").

4->Objects (including empty objects {} and arrays []).

5->Functions (e.g., function() {}, arrow functions, etc.).

6->Any BigInt value other than 0n (e.g., 1n, -123n).

7->Symbols (e.g., Symbol('id')).

8->Infinity and -Infinity. */